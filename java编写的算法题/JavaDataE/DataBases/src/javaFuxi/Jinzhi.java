package javaFuxi;

public class Jinzhi {

	public static void main(String[] args) {
		/*
		 * 0b100
		 * 0100
		 * 100
		 * 0x100
		 */
		System.out.println(0b100);  //4?二进制表示形式（b可以大写也可以小写）
		System.out.println(0100);  //64? //八进制表示形式前面加0
		System.out.println(100);  //100?//十进制
		System.out.println(0x100);  //256?//十六进制表示形式前面加0x(x可以大写也可以小写)

	}

}
 