package javaFuxi;

public class varibles {

	public static void main(String[] args) {
		System.out.println("abc");//字符串常量
		System.out.println(123);//整数常量
		System.out.println(12.3);//小数数常量
		/**
		 * ' '中必须放的是单个字符
		 */
		System.out.println('A');//单个字符常量
		//System.out.println('');//''中什么也不放是不可以的，因为代表不了任何字符
		System.out.println(' ');//相当于输出了一个空格
		
		/**
		 * boolean类中只有两个值：true和false
		 */
		System.out.println(true);
		System.out.println(false);
	
	}

}
